#include<stdio.h>

int main() {
    long long n;
    long long a[100005] = {1,1,1};
    scanf("%lld", &n);
    for(int i = 3; i <= n; i++) {
        a[i] = a[i - 1] % 1000000007 + a[i - 2] % 1000000007;
    }
    printf("%lld",a[n] % 1000000007);
} 
