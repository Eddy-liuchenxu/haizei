#include<stdio.h>

int main() {
    int a[55] = {0}, n;
    scanf("%d", &n);
    a[2] = 1, a[3] = 1, a[4] = 1;
    for(int i = 5;i <= n; i++) {
        a[i] = a[i - 2] + a[i - 3];
    }
    printf("%d", a[n]);
}
